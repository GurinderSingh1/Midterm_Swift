import Foundation
public class StudentLoanExtension : StudentLoan{
        public init(loanNumber: Int, loanAmount : Double, name : String){
            super(Self.name = name)
            super(self.loanNumber = loanNumber)
            super(self.loanAmount = loanAmount)
    }
    }
    
    

